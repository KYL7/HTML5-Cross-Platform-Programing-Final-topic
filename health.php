                 << 老年人功能性體適能測驗評估 >> </br>
                 • 身體組成／測身體質量指數（BMI）、腰圍或做身體組成分析。</br>
                 • 下肢肌耐力／</br>
                 1）坐姿瞬間起立、做20秒的膝蓋屈伸、膝蓋需要能彎屈90度。</br>
                 2）30秒椅子站立測驗。</br>
                 • 上肢肌耐力／肱二頭肌手臂屈舉，測握力剩多少；30秒鐘臂屈舉啞鈴測試，男性使用8磅重、女性使用5磅重的啞鈴。</br>
                 • 心肺耐力／</br>
                 1）2分鐘原地踏步，膝蓋抬起高度需使大腿與地平行，計算2分鐘能做幾次踏步動作。</br>
                 2）3分鐘爬樓梯測驗。</br>
                 3）走路6分鐘測行走距離。</br>
                 • 上肢柔軟度／抓背測驗。</br>
                 • 下肢柔軟度／坐在椅子上，上半身坐姿向前彎；起身再以站立姿勢前彎。</br>
                 • 靜態平衡／閉上眼睛，30秒單腳站立。</br>
                 • 敏捷性／8英呎（約250公分）椅子坐起繞物測驗。</br>
                 • 含視覺刺激的全身反應測驗／例如看運動相關的遊戲畫面並做出反應。</br>
                 • 生物電阻儀器測量身體組成／最常見的就是站立式體脂機，測量身體各種組織組成的比例，例如體脂肪或肌肉量。</p>
