                 <li>少量多餐，以點心補充營養</li>
                  <p>老年人由於咀嚼及吞嚥能力都比較差，往往一餐吃不了多少東西，而且進食時間又拖得很長。為了讓老年人每天都能攝取足夠的熱量及營養，所以營養師建議，不妨讓老年人一天分成5~6餐進食，在三次正餐之間另外準備一些簡便的點心，像是低脂牛奶泡餅乾（或營養穀片）、低脂牛奶燕麥片，或是豆花、豆漿加蛋，也可以將切小塊的水果或水果泥拌優格。</p>

                  <li>以豆製品取代部份動物蛋白質</li>
                  <p>老年人每天需要4份蛋白質，不過肉類的攝取必須限量，所以一部份蛋白質來源應該以豆類及豆製品（如豆腐、豆漿）取代。三軍總醫院營養部主任金惠民認為，老年人的飲食內容裡，每餐正餐至少要包含一份品質好的蛋白質（如瘦肉、魚肉、蛋、豆腐等），尤其是不吃肉、甚至也不吃蛋的素食者，更要由豆類及各種堅果類（花生、核桃、杏仁、腰果等）食物中獲取優質蛋白質。</br>
                  </br>
                  有些老年人聽說吃傳統板豆腐容易造成腎結石而害怕吃豆腐，台北市立慢性病防治院營養師周秀娟針對此點指出，結石並不一定是豆製品中的鈣造成的，如果出現結石症狀，應該先經由初步的尿液或腹部X光檢查找出結石原因，而不須斷然完全不吃豆腐、豆干類食品。</br>
                  </br>
                  ★怕營養吃不夠，怕一個人看醫生....長輩們擔心什麼？</p>

                  <li>主食加入蔬菜一起烹調</li>
                   <p>為了方便老年人咀嚼，儘量挑選質地比較軟的蔬菜，像是大番茄、絲瓜、冬瓜、南瓜、茄子及葉菜類的嫩葉等，切成小丁塊或是刨成細絲後再烹調。如果老人家平常吃稀飯或湯麵做為主食，那每次可以加進1∼2種蔬菜一起煮，以確保他們每天至少吃到3份（共半斤）蔬菜。</p>

                  <li>每天吃2份水果</li>
                  <p>水果是老年人常會忽略攝取的食物。從今年公布的營養調查中也看出，台灣老年人不論男性或女性，每天都吃不足2份水果。</p>
                  
                  <li>補充維生素B群</li>
                  <p>無論生病、服藥或是手術過後，都會造成維生素B群大量流失，因此對於患病的老年人來說，需要特別注意補充維生素B群。</br>
                  </br>
                  今年公布的全國老人營養健康狀況調查也發現，有相當比例的台灣老年人維生素B6攝取不足。主持這次調查的中央研究院生物醫學科學研究所潘文涵教授指出，過去國人都比較忽視維生素B群的攝取和重要性，但是近年來的研究逐漸顯示，維生素B群和老人易罹患的心血管疾病、腎臟病、白內障、腦部功能退化（認知、記憶力）及精神健康等都有相當密切的關聯。</br>
                  </br>
                  未精緻的穀類及堅果中都含有豐富的維生素B群，所以在為老年人準備三餐時，不妨加一些糙米、胚芽米或小麥胚芽等和白米一起煮成稀飯，或者也可以將少量堅果放進果汁機裡打碎成粉，加到燕麥裡一起煮成燕麥粥，金惠民主任建議。</p>

                  
                  <li>限制油脂攝取量</li>
                  <p>老年人攝取油脂要以植物油為主，避免肥肉、動物油脂（豬油、牛油），而且也要少用油炸的方式烹調食物。另外，甜點糕餅類的油脂含量也很高，儘量少讓老人家吃這一類的高脂肪零食。</br>
                  </br>
                  營養師周秀娟進一步表示，將多元不飽和脂肪（如玉米油、葵花油）和單元不飽和脂肪（如橄欖油、花生油）輪流換著吃，這樣比較能均衡攝取各種脂肪酸。</p>

                  
                  <li>少加鹽、味精、醬油，善用其他調味方法</li>
                  <p>味覺不敏感的老年人吃東西常覺得索然無味，食物一端上來就猛加鹽巴、頻沾醬油，很容易吃進過量的鈉，埋下高血壓的風險。其實味道並不只有「鹹」味一種，善用酸味或某些食材特有的香氣，就可以讓料理少鹽也美味。
                  一些具有強烈風味的蔬菜可以多利用，例如九層塔、香菜、香菇、洋蔥，用來炒蛋（如九層塔炒蛋）或是煮湯、煮粥。而利用白醋、水果醋、檸檬汁、柳橙汁或是鳳梨等各種果酸味，也可以變化料理的味道。</br>
                  </br>
                  中藥材也是可隨手運用的調味品，尤其像氣味濃厚的當歸、肉桂、五香、八角或者香甜的枸杞、紅棗等，加少許來取代鹽或醬油，豐富的味道也有助勾起老年人的食慾。</br>
                  </br>
                  如果老人家就是改不了沾醬油的習慣，營養師周秀娟則提供一個妙方，將醬油加一半的水稀釋後再沾，如此就能減少鹹度和鈉含量。
                  </p>

                  <li>少吃辛辣食物</li>
                  <p>雖然辛辣香料能挑起食慾，但是台北市立中醫醫院陳朝宗醫師卻指出，老年人如果吃多了這類食物，容易「耗陰」，造成體內水分、電解質不平衡，會出現口乾舌躁、火氣大、睡不好等症狀。</p>

                  <li>白天多補充水分</li>
                  <p>因為擔心尿失禁或是夜間頻跑廁所，不少老年人幾乎整天不大喝水，如此一來會讓原本就有便秘症狀的人更是雪上加霜。</br>
                  </br>
                  其實應該鼓勵家裡的老人在白天多喝白開水，有時候也泡一些花草茶（儘量不放糖）變化口味，但是要少喝含糖飲料。晚餐之後，就減少攝取水分，這樣就可以避免夜間上廁所、影響睡眠的困擾。</p>

                  <li>每天服用一顆綜合維他命礦物質</li>
                  <p>老年人的個體差異很大，加上可能罹患各類疾病又長期服藥，所以每個人額外需要的營養素也大不相同。綜合營養專家的建議，讓老年人每天服用一顆綜合維他命礦物質是最基本且安全的強化營養方法，尤其可以補充老年人特別需要的維生素B群、抗氧化維生素C、E及維持骨本的礦物質鈣、增強免疫力的鋅等。</br>
                  </br>
                  不過，高劑量的單一補充劑就不建議擅自服用，尤其是脂溶性的維生素A、D、E等，吃得過多會累積在體內，甚至引發毒</p>

