                 <p>任何年齡，每個人每天，都該維持有30～60分鐘中等程度的運動，身體的筋骨活動，並不需很激烈去做才能獲得健康成效，許多人將運動融入生活中，讓習慣成自然，就是很好的健康養成，特別是65歲以上的銀髮族。規律運動可以改善許多心理困境，保留認知功能，減低沮喪症狀或行為發生；許多與老化有關的功能性退化，透過運動能改善身體的柔軟度，增加姿勢的穩定性；改善骨質健康或密度，減少骨質疏鬆症的危險因素，降低跌倒和相關傷害的危險性。重要的是能帶來更健康、獨立的生活方式，改善身體功能與日常生活品質。</br>
                 對老人家來說，有氧運動、低阻抗運動、以及保持柔軟度與平衡的這三項運動，是非常必要的。沒有運動習慣的老人，有氧適能的衰退幅度，是有運動者的兩倍，所以老年人應多接受耐力或阻力性的運動訓練，常做增加柔軟度的伸展操，才會有能力在意外發生時，有較靈活的應變能力。</br>

                 建議老年人在運動前，先讓醫師做健康檢查，尤其如果選擇的運動是較激烈的，指的是超出平時走路或爬樓梯時喘的程度。醫師要做的健康檢查項目主要可能針對心肺功能檢測，以確保老年人運動的安全。如果運動屬於較低強度的，不超出平時走路或爬樓梯喘的程度，仍然可以先諮詢過醫師，並了解自己的身體狀況比較安全。若是關節有問題的老人，宜做不負重或低負重運動，像騎腳踏車、屈膝仰臥起坐等地板運動。每個人對運動的反應皆不相同，在做不同運動時，如果覺得不適時，要立即停止運動，若現場有人，要馬上告知求援。</br>
